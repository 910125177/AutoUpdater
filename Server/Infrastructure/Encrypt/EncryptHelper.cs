        /// <summary>
        /// 获取CRC16校验码计算(清研讯科对接用的校验)
        /// 备忘：
        /// 去掉首尾各2个固定字节取中间的byte数组进行计算得出一个2字节的数组就是CRC16校验结果。
        /// 放到crc位上传给定位，定位那边根据同样的算法得出校验码，如果和我们发过去的一样说明数据是完整的，否则数据不完整就丢弃数据，防止报警是误报。
        /// </summary>
        /// <param name="frame"></param>
        /// <param name="len"></param>
        /// <returns></returns>
        public static short GetCRC16(byte[] frame, Int32 len)
        {
            byte ucCRCHi = 0xFF;
            byte ucCRCLo = 0xFF;
            int iIndex;

            for (int i = 0; i < frame.Length; i++)
            {
                iIndex = ucCRCLo ^ frame[i];
                ucCRCLo = (byte)(ucCRCHi ^ aucCRCHi[iIndex]);
                ucCRCHi = aucCRCLo[iIndex];
                frame[i]++;
            }

            return (short)((short)ucCRCHi << 8 | ucCRCLo);
        }
