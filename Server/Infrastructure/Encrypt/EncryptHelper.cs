        #region 床垫厂商CRC8数据包校验
        /// <summary>
        /// 床垫CRC8校验
        /// </summary>
        /// <param name="buffer"></param>
        /// <returns></returns>
        public static byte GetCRC8(byte[] buffer)
        {
            byte crc = 0;
            for (int j = 0; j < buffer.Length; j++)
            {
                crc ^= buffer[j];//安位疑惑
                for (int i = 0; i < 8; i++)
                {
                    if ((crc & 0x01) != 0)
                    {
                        crc >>= 1;
                        crc ^= 0x8c;
                    }
                    else
                    {
                        crc >>= 1;
                    }
                }
            }
            return crc;
        }
